foreach ($centroids as $kd4 => $info) {
    [$lat, $lng, $nm] = $info;
    
    // Jika ada data, gunakan data dari db, jika tidak buat default
    $data = $kabMap[$kd4] ?? ['nmkab' => $nm, 'kawasan' => [], 'total' => 0];
    
    // Buat polygon segi-8 sebagai "bubble" (radius ~0.3 derajat)
    $radius = 0.30;
    $coords = [];
    for ($i = 0; $i <= 8; $i++) {
        $angle = ($i / 8) * 2 * M_PI;
        $coords[] = [
            round($lng + $radius * cos($angle), 5),
            round($lat + $radius * sin($angle), 5)
        ];
    }
    
    $features[] = [
        'type' => 'Feature',
        'properties' => [
            'kode'   => $kd4,
            'nmkab'  => $data['nmkab'] ?: $nm,
            'kawasan'=> $data['kawasan'],
            'total'  => $data['total']
        ],
        'geometry' => [
            'type'        => 'Polygon',
            'coordinates' => [$coords]
        ]
    ];
}

$conn->close();

echo json_encode([
    'type'     => 'FeatureCollection',
    'features' => $features
