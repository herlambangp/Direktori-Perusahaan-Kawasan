    if (!isset($kabMap[$kd])) {
        $kabMap[$kd] = ['nmkab' => $row['nmkab'], 'kawasan' => [], 'total' => 0];
    }
    $kabMap[$kd]['kawasan'][] = ['nama' => $row['nmkw'], 'jml' => (int)$row['jml']];
    $kabMap[$kd]['total'] += (int)$row['jml'];
}

